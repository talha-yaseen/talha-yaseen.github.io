	<h3>Team cap</h3>
	<p>Maximum 4 participants per delegation.</p>
	<h3>Procedure</h3>
	<ul><li><b>Round 1:</b> Each participant will perform a prepared instrumental piece. No backing track is allowed.</li><li><b>Round 2:</b> The Final Round will consist of the top 6 participants performing an original piece. A backing track is allowed so long as it doesn't interfere with the instrumental piece. The performance must not exceed 5 minutes (sound-check inclusive).</li></ul>
	<h3>Rules</h3>
	<ul><li>Each participant must perform alone.</li><li>Any backing tracks must be provided prior to the commencement of the Final Round.</li><li>Medleys are not allowed.</li></ul>
	<h3>Judging criteria</h3>
	<ul><li>Rhythm/Tempo</li><li>Expression</li><li>Originality</li></ul>
	<h3>Awards</h3>
	<ul><li>First place</li><li>Runner-up</li></ul>
